""" LENGTH OF A STRING """
Name = "Knight"
print("Name contains", len(Name), "letters.")
print(Name[0:4]) # including 0 but not 4
print(Name[1:3]) # including 1 but not 3
print(Name[1:-4]) # including 1 but not -4
print(Name[3:-2]) # including 3 but not -2
print(Name[-6:-2]) # including -6 but not -2
